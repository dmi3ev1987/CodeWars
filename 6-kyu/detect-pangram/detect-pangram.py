def is_pangram(st):
    lower_str = st.lower()
    alphabet_copy = ALPHABET_COUNT.copy()
    for letter in lower_str:
        if letter in alphabet_copy:
            alphabet_copy[letter] += 1
    return 0 not in alphabet_copy.values()